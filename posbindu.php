                <h2>Selamat datang di POSBINDU-PTM</h2>

                <p>Posbindu PTM merupakan singkatan dari Pos Pelayanan Terpadu Penyakit Tidak Menular. Program ini merupakan bagian dari upaya pemerintah dalam mengatasi masalah kesehatan masyarakat, terutama terkait dengan penyakit tidak menular seperti diabetes, hipertensi, kolesterol, dan obesitas.program ini baru saja diresmikan pada tanggal 11 juni 2023, jadi program ini sangat muda usia berjalannya.</p>

                <p>target dalam program ini yaitu untuk usia 15 tahun keatas sampai kejenjang usia lansia (Lanjut Usia).</p>

                <p>Kegiatan posbindu yaitu kegiatan yang dilakukan oleh puskesmas dan dinas kesehatan untuk melakukan deteksi dini dan pecegahan penyakit tidak menular (PTM) pada masyarakat. Posbindu PTM merupakan salah satu upaya dalam perwujudan sistem kesehatan yang berbasis promotif dan preventif. Kegiatan ini dilakukan secara rutin setiap bulan dengan melibatkan masyarakat dalam melakukan pengecekan kesehatan dan mendapatkan informasi mengenai gaya hidup sehat.</p>

                <p>Tujuan diadakannya kegiatan ini yaitu untuk meingkatkan kesadaran masyarakat mengenai pentingnya deteksi dini dan pencegahan penyakit tidak menular (PTM) serta meningkatkan kualitas hidup masyarakat melalui penerapan gaya hidup sehat.</p>

                <p>Frekuensi pemeriksaan diposbindu PTM dapat bervariasi tergantung dari kndisi kesehatan peserta program dan saran dari tenaga kesehatan. Namun,secara umum pemeriksaan dilakukan minimal satu kali dalam satu bulan.</p>

